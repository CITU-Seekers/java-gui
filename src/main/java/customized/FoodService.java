    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        // Show input dialog with text "Total xxx" then Enter cash: then input, then OK and Cancel buttons

        String totalAmount = jList2.getModel().getElementAt(jList2.getModel().getSize() - 1).split("P")[1];

        String cash = JOptionPane.showInputDialog(this, "Total " + totalAmount + "\nEnter cash:", "Input", JOptionPane.QUESTION_MESSAGE);

        if (cash != null) {
            int change = Integer.parseInt(cash) - Integer.parseInt(totalAmount);

            if (change < 0) {
                JOptionPane.showMessageDialog(this, "INSUFFICIENT CASH", "Message", JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(this, "Change: " + change, "Message", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


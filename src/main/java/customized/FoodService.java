    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int totalAmount = 0;
        String selectedFoodService = jRadioButton1.isSelected() ? "Dine In" : "Take Out";
        ArrayList<String> selectedToppings = new ArrayList<>();

        if (jCheckBox1.isSelected()) {
            selectedToppings.add(jCheckBox1.getText());
            totalAmount += 40;
        }
        if (jCheckBox2.isSelected()) {
            selectedToppings.add(jCheckBox2.getText());
            totalAmount += 30;
        }
        if (jCheckBox3.isSelected()) {
            selectedToppings.add(jCheckBox3.getText());
            totalAmount += 50;
        }

        String selectedSize = jList1.getSelectedValue();

        if (selectedSize != null) {
            totalAmount += Integer.parseInt(selectedSize.split("P")[1]);
        }

        String selectedCrust = jComboBox1.getSelectedItem().toString();
        if (selectedCrust != null) {
            totalAmount += Integer.parseInt(selectedCrust.split("P")[1]);
        }

        DefaultListModel<String> model = new DefaultListModel<>();

        model.addElement(selectedFoodService);
        selectedToppings.forEach(topping -> model.addElement(topping));
        model.addElement(selectedSize);
        model.addElement(selectedCrust);
        model.addElement("\n");
        model.addElement("Total Amount: P" + totalAmount);
        jList2.setModel(model);
    }//GEN-LAST:event_jButton1ActionPerformed


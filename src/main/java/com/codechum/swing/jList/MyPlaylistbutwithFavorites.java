    public void modifyMusicList(boolean isAdd) {
        DefaultListModel<String> musicListModel = new DefaultListModel<>();
        DefaultListModel<String> favoritesListModel = new DefaultListModel<>();
        
        if (isAdd) {
            musicArrayList.add(musicTextField.getText());
        } else {
            try {
                String selectedItem = musicList.getSelectedValue();
                musicArrayList.remove(selectedItem);
                favoritesArrayList.remove(selectedItem);
            } catch (Exception e) {
                
            }
        }
        
        for (String music: musicArrayList) {
            musicListModel.addElement(music);
        }
        
        for (String music: favoritesArrayList) {
            favoritesListModel.addElement(music);
        }
        
        musicList.setModel(musicListModel);
        favoritesList.setModel(favoritesListModel);
    }
    

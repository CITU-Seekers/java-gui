    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        String receipt = "Name: " + nameTextField.getText() + "\nItems: ";
        List<String> items = new ArrayList<String>();
        double totalPrice = 0;

        if(shoesCheckBox.isSelected()) {
            totalPrice += 200;
            items.add("Shoes");
        }
        if(pantsCheckBox.isSelected()) {
            totalPrice += 150;
            items.add("Pants");
        }
        if(shirtCheckBox.isSelected()) {
            totalPrice += 120;
            items.add("Shirt");
        }

        for(int i = 0; i < items.size(); i++) {
            if (i == items.size() - 1) {
                receipt += items.get(i);
            } else {
                receipt += items.get(i) + ", ";
            }
        }

        receipt += "\nPayment Method: ";
        if(paymentMethodComboBox.getItemAt(paymentMethodComboBox.getSelectedIndex()).equals(paymentMethodComboBox.getItemAt(0))){
            receipt += "Cash";
        }
        else if(paymentMethodComboBox.getItemAt(paymentMethodComboBox.getSelectedIndex()).equals(paymentMethodComboBox.getItemAt(1))){
            receipt += "Credit Card";
        }
        else if(paymentMethodComboBox.getItemAt(paymentMethodComboBox.getSelectedIndex()).equals(paymentMethodComboBox.getItemAt(2))){
            receipt += "Debit Card";
        }

        if(defaultRadioButton.isSelected()) {
            if (totalPrice >= 300) {
                totalPrice = totalPrice - (totalPrice * 0.05);
            }
        }

        if(specialRadioButton.isSelected()) {
            totalPrice = totalPrice - (totalPrice * 0.15);
        }

        receipt += "\nTotal Cost: " + Double.toString(totalPrice) + 0;
        receiptTextArea.setText(receipt);
    }


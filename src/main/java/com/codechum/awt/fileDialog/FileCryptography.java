    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        fd.setMode(FileDialog.LOAD);
        fd.setMultipleMode(true); // Allow multiple file selection
        fd.setVisible(true);

        File[] selectedFiles = fd.getFiles();

        if (selectedFiles.length > 0) {
            StringBuilder mergedText = new StringBuilder();

            for (File inputFile : selectedFiles) {
                try (InputStream is = new FileInputStream(inputFile);
                     BufferedReader br = new BufferedReader(new InputStreamReader(is))) {

                    String str;
                    while ((str = br.readLine()) != null) {
                        mergedText.append(str).append(System.lineSeparator());
                    }

                } catch (FileNotFoundException ex) {
                    Logger.getLogger(FileMerger.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(FileMerger.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            textField1.setText(mergedText.toString().trim());
        }
    }//GEN-LAST:event_button1ActionPerformed

    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        // TODO add your handling code here:
        // Encrypt the text wherein each character is replaced by the next character in the ASCII table (e.g. A becomes B, B becomes C, etc.) and save the encrypted text to a text file
        String text = textField1.getText();
        String encryptedText = "";
        for (int i = 0; i < text.length(); i++) {
            char c = text.charAt(i);
            encryptedText += (char) (c + 1);
        }

        // Save the encrypted text to a text file 
        fd.setMode(FileDialog.SAVE);
        fd.setVisible(true);
        File outputFile = new File(fd.getDirectory(), fd.getFile()); // Corrected line

        try {
            java.io.FileWriter fw = new java.io.FileWriter(outputFile);
            fw.write(encryptedText);
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(FileCryptography.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_button2ActionPerformed

    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        // TODO add your handling code here:
        // Decrypt the text wherein each character is replaced by the previous character in the ASCII table (e.g. B becomes A, C becomes B, etc.) and display the decrypted text in the text field
        String text = textField1.getText();
        String decryptedText = "";
        for (int i = 0; i < text.length(); i++) {
            char c = text.charAt(i);
            decryptedText += (char) (c - 1);
        }

        textField1.setText(decryptedText);
        
    }//GEN-LAST:event_button3ActionPerformed


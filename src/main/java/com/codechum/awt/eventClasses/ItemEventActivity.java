    private void updateStatusLabel() {
        String statusText = "Checkbox";

        // Check the state of each checkbox and append to the statusText
        if (checkbox1.getState()) {
            statusText += " 1";
        }
        if (checkbox2.getState()) {
            statusText += (statusText.equals("Checkbox") ? "" : " and") + " 2";
        }
        if (checkbox3.getState()) {
            statusText += (statusText.equals("Checkbox") ? "" : " and") + " 3";
        }
        if (checkbox1.getState() && checkbox2.getState() && checkbox3.getState()){
            statusText = ("Checkbox 1, 2 and 3");
        }

        // Set the statusText to the label
        label1.setText(statusText + " selected");
    }
    
